package com.example.ProdutosDigitais.Produtos.model.request;

public class ProdutoDetailRequest {

    private String nome;
    private int id;
    private double price;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

}

